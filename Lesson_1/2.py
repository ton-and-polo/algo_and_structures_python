# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др.
# над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.

six = 6  # '0000 0110'
five = 5  # '0000 0101'

print(
    f'AND: {six & five}\n'  # '0000 0100'
    f'OR: {six | five}\n'  # '0000 0111'
    f'BIT_SHIFT left: {five << 2}\n'  # '0001 0100'
    f'BIT_SHIFT right: {five >> 2}\n'  # '0000 0000'
)